public class Calculator {

    public Calculator() {
    }

    public int addition (int x, int y){
        return x+y;
    }

    public int subtraktion (int x, int y){
        return x-y;
    }

    int multiplikation(int x, int y){
        return x*y;
    }

    public double division (int x, int y){

        return x/y;
    }
}
